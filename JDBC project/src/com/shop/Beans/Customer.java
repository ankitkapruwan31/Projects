package com.shop.Beans;

public class Customer {

	
	String name , lastName , email, password;
	long phoneNumber;
	int numberOfItemsPurchased;
	
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public long getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(long phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public int getNumberOfItemsPurchased() {
		return numberOfItemsPurchased;
	}
	public void setNumberOfItemsPurchased(int numberOfItemsPurchased) {
		this.numberOfItemsPurchased = numberOfItemsPurchased;
	}
	
	
	
	
	
	
}//end of customer
